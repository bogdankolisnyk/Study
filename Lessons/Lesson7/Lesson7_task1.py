def say_hi(name, age):
    return f"Hello, my name is {name} and I am {age} years old."

assert say_hi("Dean", 32) == "Hello, my name is Dean and I am 32 years old."
print(say_hi("Dean", 32))
assert say_hi("Sam", 30) == "Hello, my name is Sam and I am 30 years old."
print(say_hi("Sam", 30))
