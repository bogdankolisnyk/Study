def say_hi(name, age):
    return f"Hello, my name is {name} and I am {age} years old."

assert say_hi("Alex", 32) == "Hello, my name is Alex and I am 32 years old."
print(say_hi("Alex", 32))
assert say_hi("Frank", 68) == "Hello, my name is Frank and I am 68 years old."
print(say_hi("Frank", 68))
